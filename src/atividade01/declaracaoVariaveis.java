package atividade01;

public class declaracaoVariaveis {
    public static void main(String[] args) {
        int numero = 10;
        double preco = 19.99;
        char letra = 'A';
        boolean ativo = true;

        System.out.println("Número: " + numero);
        System.out.println("Preço: " + preco);
        System.out.println("Letra: " + letra);
        System.out.println("Ativo: " + ativo);
    }
}